package space.arim.libertybans.it;

import space.arim.libertybans.core.service.Time;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

@Retention(RUNTIME)
@Target({TYPE, METHOD})
public @interface SetTime {

	/**
	 * Sets the current unix time which will be used by {@link Time}
	 *
	 * @return the unix time to use for tests
	 */
	long unixTime();

	/**
	 * The default time if this annotation is not specified
	 *
	 */
	long DEFAULT_TIME = 1621440000L;
}
